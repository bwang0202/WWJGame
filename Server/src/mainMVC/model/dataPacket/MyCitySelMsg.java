package mainMVC.model.dataPacket;

import java.io.Serializable;
/**
 * a message representing city selections
 * it contains a list of cities avaliable
 * @author Bojun
 *
 */
public class MyCitySelMsg implements Serializable{
	/**
	 * generated uid
	 */
	private static final long serialVersionUID = -2753906978626060448L;
	/**
	 * a string of cities avaliable
	 */
	private String _cities;
	/**
	 * constructor
	 * @param city a string of cities avaliable
	 */
	public MyCitySelMsg(String city){
		_cities = city;
	}
	/**
	 * getter for the string of avaliable cities
	 * @return
	 */
	public String getCityDescription(){
		return _cities;
	}

}
