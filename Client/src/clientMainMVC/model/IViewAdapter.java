package clientMainMVC.model;

/**
 * interface between main model and main view
 * @author Bojun
 *
 */
public interface IViewAdapter {
	/**
	 * append a string in main view
	 * @param string
	 */
	public void append(String string);
	/**
	 * a factory method that produce a minimodel-miniview adapter, given a minimodel
	 * @param miniModel
	 * @return
	 */
	public IMiniModel2ViewAdapter makeMiniModel2ViewAdapter(MiniModel miniModel);
}
