package clientMainMVC.model.dataPacket;

import comp310f13.rmiChat.IAddUser;
import comp310f13.rmiChat.IUser;
/**
 * add user data, with a IUser in it
 * @author Bojun
 *
 */
public class AddUser implements IAddUser{
	private static final long serialVersionUID = 4779850891423219095L;
	private IUser _user;
	/**
	 * constructor, 
	 * @param user the IUser to store
	 */
	public AddUser(IUser user){
		_user = user;
	}
	/**
	 * stored iuser
	 */
	public IUser getUser() {
		return _user;
	}
}
